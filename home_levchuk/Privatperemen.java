package home_levchuk;

public class Privatperemen {
    // Объявление приватных переменных с присвоение им значений;
    private static int a = 1;
    private static int b = 3;
    private static int c = 9;
    private static int d = 27;

    public static void main(String[] args) {

        int result = -a + b - c + d;    // Математическое выражение с переменными;

        System.out.println(result);     // Вывод результата на экран;
    }
}
