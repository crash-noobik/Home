package home_levchuk;

public class Sumproizveden {
    public static void main(String[] args) {
        int i = 3;  // Объявление переменной с присвоением ей значения 3;
        int j = 4;  // Объявление переменной с присвоением ей значения 4;
        int k = i * i + j * j;  // Математический оператор;
        System.out.println(i + "*" + i + "+" + j + "*" + j + "=" + k);  // Вывод результата;
    }

}
